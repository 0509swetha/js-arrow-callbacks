// In JavaScript, a normal function is a
//  block of code that performs a specific
//   task when called. It is defined using the
//    function keyword followed by a name, a
//     list of parameters (optional), and a block
//      of code enclosed in curly braces {}.

function addNumbers(num1,num2){
    return num1+num2;
}
let sum=addNumbers(2,3);
